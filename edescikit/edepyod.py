def ede_abod(contamination):
    clf = ABOD(contamination=contamination)
    return clf


def ede_cblof(contamination,
              check_estimator,
              random_state):

    clf = CBLOF(contamination=contamination,
                check_estimator=check_estimator,
                random_state=random_state)
    return clf

